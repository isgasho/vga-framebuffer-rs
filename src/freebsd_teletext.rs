/// has been modified to include Teletext 'sixels' (block graphic glyphs)
/// instead of the usual extended ASCII characters from Code Page 850.
///
/// A sixel has six segments:
///
/// +-----+-----+
/// |     |     |
/// +-----+-----+
/// |     |     |
/// +-----+-----+
/// |     |     |
/// +-----+-----+
///
/// Each segment is 4 pixels across and 5 or 6 pixels high (the middle ones
/// are 1 pixel higher).
///
